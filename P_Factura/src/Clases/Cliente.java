package Clases;

public class Cliente {

    private String CI;
    private String nombre;
    private String direccion;
    private String telefono;
    private String email;

    public Cliente (String CI, String nombre, String dir, String telf, String email){

        this.CI = CI;
        this.nombre = nombre;
        this.direccion = dir;
        this.telefono = telf;
        this.email = email;

    }

}
